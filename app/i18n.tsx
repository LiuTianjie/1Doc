const fallback = messages.en;

function detectLocale(): Locale {
  if (typeof navigator === "undefined") {
    return "zh";
  }

  const saved = window.localStorage.getItem("1doc:locale");
  if (saved && locales.includes(saved as Locale)) {
    return saved as Locale;
  }

  const preferred = navigator.language.toLowerCase().split("-")[0];
  return locales.includes(preferred as Locale) ? (preferred as Locale) : "en";
}

type I18nContextValue = {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  localeLabel: string;
  t: (key: MessageKey, values?: Record<string, string | number>) => string;
};

const I18nContext = createContext<I18nContextValue | null>(null);

export function I18nProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>("zh");

  useEffect(() => {
    setLocaleState(detectLocale());
  }, []);

  useEffect(() => {
    document.documentElement.lang = locale === "zh" ? "zh-CN" : locale;
    window.localStorage.setItem("1doc:locale", locale);
  }, [locale]);

  const value = useMemo<I18nContextValue>(() => {
    function t(key: MessageKey, values: Record<string, string | number> = {}): string {
      const dictionary = (messages as Partial<Record<Locale, Partial<Record<MessageKey, string>>>>)[locale] ?? fallback;
      const template = dictionary[key] ?? fallback[key] ?? key;
      return Object.entries(values).reduce(
        (text, [name, replacement]) => text.replaceAll(`{${name}}`, String(replacement)),
        template
      );
    }

    return {
      locale,
      setLocale: setLocaleState,
      localeLabel: localeLabels[locale],
      t
    };
  }, [locale]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error("useI18n must be used inside I18nProvider.");
  }
  return context;
}

export function LocaleSwitcher() {
  const { locale, setLocale, t } = useI18n();

  return (
    <label className="locale-switcher" aria-label={t("common.uiLanguage")}>
      <span>{locale.toUpperCase()}</span>
      <select value={locale} onChange={(event) => setLocale(event.target.value as Locale)}>
        {locales.map((value) => (
          <option key={value} value={value}>
            {localeLabels[value]}
          </option>
        ))}
      </select>
    </label>
  );
}
